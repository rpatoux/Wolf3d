#include "libft.h"

int		ft_atoi(char const *str, int *k)
{
	int		nb;
	int		sign;

	nb = 0;
	sign = 1;
	while (str[*k] && (str[*k] == ' ' || str[*k] == '\n' || str[*k] == '\t' ||
		str[*k] == '\f' || str[*k] == '\v' || str[*k] == '\r'))
		(*k)++;
	if (str[*k] && (str[*k] == '-' || str[*k] == '+' || str[*k] == ' '))
	{
		if (str[*k] == '-')
			sign = -1;
		(*k)++;
	}
	while (str[*k] && str[*k] >= '0' && str[*k] <= '9')
	{
		nb = nb * 10 + str[*k] - '0';
		(*k)++;
	}
	(*k)--;
	return (sign * nb);
}
