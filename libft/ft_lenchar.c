#include "libft.h"

int		ft_lenchar(char *str, char c)
{
	int i;
	int	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] != c)
		{
			j++;
			while (str[i] != c && str[i])
				i++;
			i--;
		}
		i++;
	}
	return (j);
}
