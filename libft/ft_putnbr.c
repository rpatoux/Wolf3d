#include "libft.h"

void	ft_putnbr(int nb)
{
	char	print;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "2", 1);
			nb = 147483648;
		}
		else
			nb = nb * -1;
	}
	if (nb <= 9)
	{
		print = '0' + nb;
		write(1, &print, 1);
	}
	else
	{
		ft_putnbr(nb / 10);
		print = '0' + nb % 10;
		write(1, &print, 1);
	}
}
